const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Static method to find user by phone number
userSchema.statics.findUserByPhone = function(phone) {
  return this.findOne({ phone: phone });
};

module.exports = mongoose.model("User", userSchema);
